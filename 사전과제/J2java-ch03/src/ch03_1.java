public class ch03_1 {
    public static void main(String[] args) {
        // ========== 03 - 1 : 숫자 ==========

        int i = 0;
        System.out.println(i++); // println 실행 후 i 증가
        System.out.println(i); // 증가된 i 값 출력

        System.out.println(++i); // println 실행 전 i 증가, 증가된 i 값 출력
        System.out.println(i); // 증가된 i 값 출력
    }
}
